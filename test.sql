  `tag_id` BIGINT NOT NULL,
  `certificate_id` BIGINT NOT NULL,
  PRIMARY KEY (`tag_id`, `certificate_id`),
    FOREIGN KEY (`tag_id`)
    REFERENCES `mydb`.`tags` (`id`)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    FOREIGN KEY (`certificate_id`)
    REFERENCES `mydb`.`certificates` (`id`)
